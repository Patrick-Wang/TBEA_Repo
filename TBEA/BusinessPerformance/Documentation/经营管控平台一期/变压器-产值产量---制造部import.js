function nextMonth(d){
	var dt;
	if (d.getMonth() == 11){
		dt = d.getFullYear() + 1 + "-1";
	}else{
		dt = d.getFullYear() + "-" + (d.getMonth() + 2);
	}
	dt = dt + "-" + d.getDate();
	return dt;
}

